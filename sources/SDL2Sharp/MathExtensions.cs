namespace SDL2Sharp
{
    public static class Math
    {
        public static int Mod(int a, int b)
        {
            var result = a % b;
            if (result < 0)
            {
                result += b;
            }
            return result;
        }

        public static int NextPowerOfTwo(int value)
        {
            --value;
            value |= value >> 1;
            value |= value >> 2;
            value |= value >> 4;
            value |= value >> 8;
            value |= value >> 16;
            ++value;
            return value;
        }
    }
}
